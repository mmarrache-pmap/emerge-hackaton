import { shallowComponentWithProps } from '../test_helper';
import PDF from '../../src/components/PDF';

describe('(Component) <PDF />', () => {
  let props = {
    file: 'pdf-sample.pdf'
  };

  let component;

  beforeEach(() => {
    component = shallowComponentWithProps(PDF, props);
  });

  it('Should be present', () => {
    expect(component).to.be.present();
  });

  it('Should render as a div', () => {
    expect(component.type()).to.equal('div');
  });

  it('Should render with \'pdf-viewer\' className', () => {
    expect(component.hasClass('pdf-viewer')).to.equal(true);
  });

  it('Should have a <React-PDFjs/> descendant', () => {
    expect(component).to.have.descendants('React-PDFjs');
  });

  it('<React-PDFjs /> descendant should preview \'pdf-sample.pdf\'', () => {
    expect(component.find('React-PDFjs')).to.have.prop('file').to.equal('pdf-sample.pdf');
  });
});
